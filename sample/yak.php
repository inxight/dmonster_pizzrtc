의한 개정약관의 공지기간 내에 "당사" 에 
송신하여 "당사"의 동의를 받은 경우에는 
개정약관 조항이 적용됩니다.

5. 이 약관에서 정하지 아니한 사항과 이 
약관의 해석에 관하여는 정부가 제정한 전자
거래소비자보호지침 및 관계법령 또는 상관례에
따릅니다.

제 4 조(서비스의 제공 및 변경)

1."당사"는 다음과 같은 업무를 수행합니다.

1)서비스에 대한 정보 제공 및 구매계약의 체결
2)구매계약이 체결된 서비스를 제공합니다.
3)기타 "당사"가 정하는 업무

2."당사"는 기술적 사양의 변경 등의 경우에는 
장차 체결되는 계약에 의해 제공할 재화.용역
내용을 변경할 수 있습니다. 이 경우에는 변경
된 재화 . 용역의 내용 및 제공일자를 명시하여
현재의 재화.용역의 내용을 게시한 곳에 그 
제공일자 이전 7일부터 공지합니다.

3."당사"가 제공하기로 이용자와 계약을 체결한
서비스의 내용을 재화의 품절 또는 기술적 
사양의변경 등의 사유로 변경할 경우에는 
"당사"는 이로 인하여 이용자가 입은 손해를 
배상합니다.

단, "당사"에 고의 또는 과실이 없는 경우에는
그러하지 아니합니다.

제 5조(서비스의 중단)
 
1."당사"는 컴퓨터 등 정보통신설비의 보수점검. 
교체 및 고장, 통신의 두절 등의 사유가 발생한 
경우에는 서비스의 제공을 일시적으로 중단 할 
수 있습니다.

2.제1항에 의한 서비스 중단의 경우에는 "회사"
는제8조에 정한 방법으로 이용자에게 통지
합니다.
3."당사"는 제 1항의 사유로 서비스의 제공이 
일시적으로 중단됨으로 인하여 이용자 또는 
제 3자가 입은 손해에 대하여 배상합니다.

단, "당사"에 고의 또는 과실이 없는 경우에는 
그러하지 아니합니다.

제 6조(회원가입)
  
1.이용자는 "당사"가 정한 가입 양식에 따라 
회원정보를 기입한 후 이 약관에 동의한다는 
의사표시를 함으로서 회원가입을신청합니다.

2. "당사"는 제 1항과 같이 회원으로 가입할 
것을 신청한 이용자 중 다음 각 호에 해당하지 
않는 한 회원으로 등록합니다.

1)가입신청자가 이 약관 제 7조 제 3항에 의하
여 이전에 회원자격을 상실한 적이 있는 경, 
다만, 제 7조 제 3항에 의한 회원자격 상실 후
3년이 경과한 자로서 "당사"의 회원재가입 
승낙을 얻은 경우에는 예외로 한다.
2)등록 내용에 허위, 기재누락, 오기가 있는 
경우
3)기타 회원으로 등록하는 것이 "당사"의 기술
상 현저히 지장이 있다고 판단되는 경우
3.회원가입계약의 성립시키는 "당사"의 승낙이 
회원에게 도달하는 시점으로 합니다.

4.회원은 제 15조 제 1항의 의한 등록사항에 
변경이 있는 경우, 즉시 전자우편 기타 방법
으로 "당사"에 대하여 그 변경 사항을 
알려야 합니다.


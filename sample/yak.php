2. "당사"는 이용약관 의 규제에 관한 법률, 
전자거래기본법, 전자서명법, 정보통신망 
이용촉진등에 관한법률, 소비자보호법 등 관련
법을 위배하지 않는 범위에서 이 약관을 개정할
수 있습니다.

3. "당사"가 약관을 개정할 경우에는 적용일자
및 개정사유를 명시하여 현행약관과 함께 초기
화면에 그 적용일자 7일 이전부터 적용일자 
전일까지 공지합니다.

4. "당사"가 약관을 개정할 경우에는 그 개정
약관은 그 적용일자 이후에 체결되는 계약에만

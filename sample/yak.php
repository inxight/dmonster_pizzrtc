1.회원은 "당사"에 언제든지 탈퇴를 요청할 수
있으며 "당사"는 즉시 회원탈퇴를 처리합니다.

2.회원이 다음 각 호의 사유에 해당하는 경우,
"당사"는 회원자격을 제한 및 정지시킬 수 
있습니다.

1) 가입 신청 시에 허위 내용을 등록한 경우
2)"당사"를 이용하여 서비스의 대금, 기타 
"당사"이용에 관련하여 회원이 부당하는 채무
를 기일에 이행하지 않은 경우

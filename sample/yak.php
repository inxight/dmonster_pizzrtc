경우
4)"당사"를 이용하여 법령과 이 약관이 금지
하거나 공서양속에 반하는 행위를 하는 경우

3."당사"가 회원 자격을 제한, 정지 시킨 후,
동일한 행위가 3회 이상 반복되거나 30일
이내에 그 사유가 시정되지 아니하는 경우 
"당사"는 회원자격을 상실 시킬 수 있습니다.

4."당사"가 회원자격을 상실시키는 경우에는 
회원등록을 말소합니다. 이 경우 회원에게 
이를 통지하고, 회원등록 말소 전에 소명할 
기회를 부여합니다.


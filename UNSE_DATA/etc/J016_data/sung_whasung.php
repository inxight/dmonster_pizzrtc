             <li>화성은 육살성중의 하나로 양화(陽火)에 속하며 짝성으로 영성(鈴星)이 있으며 합쳐서 화령이라 부른다.
	     <li>화성이 명궁에 있으면 화상을 입는 수가 있다.
             <li>화성이 명궁에서 태양과 동궁하면 일생동안  노력은 많이 하나 결과가 없다.
	     <li>화성이 거문과 동궁하면 화거격으로 일생 구설이 많으며 여명이면 감정의 기복이 심하여 불륜에 빠지기도 한다.
	     <li>화성이 탐랑과 동궁하면 화탐격이 되어 두 별이 묘왕하면 귀하게 되고 특수한 재능이나 기예가 있으며 중년이후에 횡발, 횡재하는 기회가 있다. 
	     <li>여명이 함지에서 화성을 수신명(명궁이나 신궁에 있음)하면 음천하며 형부극자한다.(남편과 자식을 극한다.)
	     <li>화성이 파군과 동궁하면 일생 노력은 많이하나 얻는게 없고 칠살과 동궁하면 기술이나 기예를 가지게 된다. 
	     <li>화성이 묘왕하고 천마와 동궁하면 전마의 형국으로 교통사고등의 위험이 있다.
	     <li>寅, 午, 戌궁에서 화성이나 영성이 단수하고(주성없이 혼자 있고) 삼방대궁에서 길성이 보이면 군 계통에서 무공을 세우며 이름을 떨친다.
	     <li>화성, 영성이 협명하고(명궁의 좌,우궁에 있고) 경양, 타라가 동궁하거나 가회하면 유년시절 심한 병을 앓거나 신체장애자가 되기쉽고 일생이 외롭고 파란만장하다.
	     <li>파군과 화성이 명운에서 동궁하면 파산의 운이 있다.
	     <li>화성이나 영성이 命궁에 있고 삼방사정에서 염정, 칠살, 경양이 모두 보이면 큰 사고나 재난이 닥친다.
             <li>화성이나 영성이 命궁에 있고 삼방사정에서 무곡, 타라, 문창이 모두 보이면 물로 인한 재난을 겪거나 익사의 위험이 있다.
             <li>화성이나 영성이 命궁, 천이궁에서 서로 마주보거나 삼방에서 경양, 타라가 보이면 신체장애를 일으킨다.
	     <li>화성이 명궁에서 무곡과 동궁하면서 경양이나 타라가 상협하거나(명궁의 좌, 우궁에 있거나) 대궁에서 충을 하면 재물로 인하여 목숨이 위태롭다.  
	     <li>화성이나 영성이 묘왕하면서 명궁에 단수하면(주성없이 홀로 있으면) 부귀한 운이라고 보는데 조년에는 고생이 심하다. 이때 탐랑이 대궁 천이궁에서 대조하면 해액이 된다.(액을 떼운다) 


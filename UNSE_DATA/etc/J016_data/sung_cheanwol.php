             <li>천월은 육길성중의 하나로  음화(陰火)에 속하며 옥당귀인에 해당되는 귀인성으로 음성적으로 도와주는 별이다.
	     <li>천월은 짝성으로 천괴가 있으며 합쳐서 괴월이라 부른다.
	     <li>짝성으로 천괴(天魁)가 있으며 실력있는 후원자(장배귀인 : 손윗사람이나 선배, 상사)로부터 기회가 주어지며 발탁, 공천등 본인의 노력과는 관계가 없는 호기를 만난다. 
             <li>천괴, 천월이 명, 신궁에 나뉘어 있거나 명궁을 상협하면 문장력이 뛰어나고 일생동안 귀인의 도움을 받는다.
	     <li>천월이 명궁에 있고 문창, 문곡, 좌보, 우필, 태양, 태음등이 동궁하면 어려서 부터 이름을 날리며 큰 시험에 합격하는 운이 있고 현모양처를 만난다.
	     <li>천월은 음의 성이므로 태양, 천동, 탐랑, 천상, 천량등의 양의 성과 동궁하는것이 좋은데 이를 반대로 만나거나, 동궁하는 주성이 함지이면 기회가 와도 놓치기 쉽다.
	     <li>여명이면서 명궁이 신(申)궁이고 기년생(己年生)이면서 3월, 9월, 11월생이면 지체가 높은사람의 귀부인이 될 명이다.
	     <li>행운에서 대괴(大魁)나 대월(大鉞), 유괴(劉魁)나 유월(流鉞)이 중첩되면 천월의 특징이 강력히 발휘되어 제도나 시행령등이 변화되어서 얻어지는 갑작스런 이익이 많다고 보나 중년 이후 흉성이 많이 가회하는 운이 올 때는 지병을 얻거나 고난이 닥치는 경우가 있다.
	     <li>축(丑), 미(未)궁에 괴월이 있으면 좌귀향귀(左貴向貴)라고 하여 부귀가 매우 크다고 보나 살성이 동궁하거나 가회하면 소인의 방해가 있고 중년 이후에는 고질병을 앓는 경우가 있다.
	     <li>천월이 명궁에 있고 여명의 경우 탐랑, 함지, 목욕, 천요, 천희, 홍란등의 도화성이 동궁하면 불륜으로 빠지기 쉽다. 


	     <tr bgcolor=ffffff> <td>
             <ul>
             <li>문창은 육길성중의 하나로 음금(陰金)에 속하며 짝성으로 문곡(文曲)이 있고 합쳐서 창곡이라 부른다.
	     <li>창곡은 귀인성으로 부하, 후배, 제자, 손아랫 사람등에서 나타나는 만배귀인(원조자)을 말한다.
             <li>문창에 화기가 붙으면 흉한 일이 발생하는데 부모궁에서 그러하면 문서로 인한 사기를 당할 운이거나 집안에 누군가가 죽어 상복을 입을 운이 있으며 재백궁에서 그러하면 수표등이 부도가 나 손재를 볼 운이다.
	     <li>또한 형제궁이나 노복궁에서 화기와 동궁하면 가까운 사람으로 부터 배신이나 사기를 당하며 부처궁에서 그러하면 이혼과 관련이 있다.
	     <li>문창에 화과가 붙으면 각종 시험에 합격할 운이 있고 승진이나 취직등이 잘 되며 문서계약등이 잘 이루어지는 운이다.
	     <li>여명이 명궁의 삼방사정에서 창곡을 보고 도화성이 보이면 미인 박복의 명이다.
	     <li>문창이 묘왕지에 있고 삼방사정에서 길성이 모이면 재관이 뚜렸하고 부귀하며 과거급제의 운이 있으나 실함지에 있고 살성이 모이면 이름없는 달변가에 불과하다.
	     <li>천기가 함지에서 문창과 동궁하면  생활이 안정되지 못하고 허세가 많으며 거짓을 좋아하게 된다.
	     <li>寅궁이나 卯궁에서 파군과 동궁하면 인생에 파고가 많고 亥, 子, 丑궁에서 문곡화기가 파군과 동궁하면 일생이 고생스럽고 허망하다.
	     <li>巳, 亥궁에서 탐랑과 동궁하면 하는 일이 거꾸로 되거나 갑작스러운 죽음을 당할 수 있다.
	     <li>염정이 사해궁에 있으면서 창곡이 명운으로 오는 행운에서는 목숨이 위험하다. 
	     <li>문창화기가 辰, 戌궁에 있으면서 삼방사정에서 무곡, 영성, 타라가 모이면 영창타무격(鈴昌陀武格)으로 물과 관련된 사고를 당하거나 하는 일이 큰 낭패를 보게 된다.  
	     <li>태양이 卯궁에서 묘왕지에 있으면서 삼방사정에서 천량, 문창, 녹존, 화록이 보이면 양양창록격(陽梁昌祿格)이 되어 시험에 합격하거나 입찰경쟁등에서 좋은 결과를 보게 된다.
	     <li>문창화과와 염정화기가 동궁하면 주색문제로 말썽을 일으킨다.
             <li>여명은 명궁에 창곡이  있거나 천이궁에 창곡이 있으면 부귀를 누리지만 불륜에 빠지기 쉽다.




	     <li>천월(天鉞)과 천귀가 동궁하고 삼방에서 화록,녹존을 만나고 정성이 묘왕하면  지위로 인하여 큰 돈을 벌거나 귀인의 도움으로 재물을 얻는다.
	     <li>천귀는 도화성과 동궁하거나 가회하면 화류계(연예계)에서 유명해지고 이성에게 인기가 있으며 천요(天姚)와 동궁하면 겉만 화려하다. 



             <li>천형은 소흉성으로 양화(陽火)에 속하며 형극을 주관하며 의약과 관련이 있다.
	     <li>寅, 卯, 酉, 戌궁은 묘지이고 丑, 未궁은 함지이다.
	     <li>태양이 寅, 卯궁에서 천형과 동궁하면서 수명하면(명궁이면) 군이나 경찰, 사법계통과 관련이 있다.
	     <li>천형이 丑, 未궁의 함지에 있으면 관재, 질병, 사고, 요절등의 악재가 발생한다. 
	     <li>대한이나 유년에서 천형을 만나면 수술을 하거나 혈광지재가 일어난다.
	     <li>천형이 寅, 卯, 酉, 戌궁에서 자미, 천부, 문창, 문곡, 천괴, 천월, 무곡, 천상등과 동궁하고 수신명(命궁이거나 身궁이면)하면 대업을 이루며 권세를 얻는다.
             <li>여명의 경우 천형을 丑, 未궁의 함지에서 수명하고(명궁에 있고) 주성이 없거나 살성, 탐랑, 목욕등과 동궁하면 형부극자하게 되며 후처가 될 운이 있다.
             <li>유년운에서 천형, 상문, 비렴이 동궁가회하면 상복(喪服)을 입는 경우가 생긴다.
	     <li>천형이 질액궁에서 살성과 동궁하면 지체부자유가 되기 쉽고 백호, 홍란, 칠살, 염정등과 동궁하면 수술이나 혈광지재가 있다.
	     <li>관록궁이나 재백궁에서 천형이 살성을 보면 직업을 바꾸거나 사업이 부도가 나며 재무관계로 관재가 발생한다.
	     <li>천형이 부처궁에 거하면 부부간에 문제나 불화가 생겨 시끄럽다.






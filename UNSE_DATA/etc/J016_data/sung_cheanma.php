             <li>천마는 길성으로 양화(陽火)에 속하며 짝성으로 녹존이 있고 합쳐서 녹마라 부른다.
	     <li>천마가 녹존이나 화록과 命,身궁에서 동궁하면 녹마교치라 하여 부귀가 쌍전한다.
	     <li>천마가 자미나 천부와 동궁하면 부여마(扶輿馬)라 하여 바쁜 중에 힘을 집결하여 소득이 있으며 타향에서 귀인의 도움을 받으며 일찌기 고시에 합격하며 중년 이후에는 부러움이 없다.
	     <li>천마가 태음과 동궁하면 자마(雌馬)라 하여 소극적이고 피동적이며 대체로 늦게 발복한다.
	     <li>천마가 태양과 동궁하면 웅마(雄馬)라 하여 능동적이고 적극성이 있으며 성공을 보장한다.
	     <li>무곡이나 천상이 인신궁에서 동궁하고 천마를 보면 재인좌마(財印坐馬)라 하여 명성과 지위, 신분이 매우 높게 된다.
	     <li>천마가 공망지에 임하면 사망마(死亡馬)라 하여 평생 분주하지만 소득이 없고 일을 도모하여도 실패하기가 쉽다. 
	     <li>자미와 칠살이 巳, 亥궁에서 수명하고 천마와 동궁하면 권마(權馬)라 하여 권세를 장악하여 실권을 쥐고 경영을 좌지우지한다.
	     <li>천마가 화성과 동궁하면 전마(戰馬)라 하여 용맹, 충동적이 되고 주성이 묘왕하고 길성이 가회하면 횡발의 기회가 있으나 그렇지 않으면 요절, 사고, 신체이상, 상처등과 관련이 있다.  
	     <li>천마가 타라와 동궁하면 절족마(折足馬)라 하여 호사다마하며 일생동안 하는 일이 장애가 많고 성취가 약하며 제동이나 방해, 배신이 끊이질 않는다. 외출이나 원행에 불리하고 소인의 모함이나 음해를 받는다.
	     <li>천마가 부처궁에서 타라와 동궁하고 주성이 불리하면 이혼수가 있으며 천이궁에서 그러하면 교통사고가 있다.
	     <li>천마가 칠살과 동궁하고 삼방에서 경양, 타라가 가회하면 부시마(負屍馬)라 하여 노력을 하여도 헛수고가 되며 화성, 영성, 화기, 백호등의 악성을 만나면 불의의 사고로 몸을 상하게 된다.
	     <li>천마가 화기와 동궁하면 병마(病馬)라 하여 앞길이 암담하여 갈피를 잡지 못하고 희망이 없다.
	     <li>천마가 巳궁에서 비렴을 만나면 천마승풍(天馬乘風)이라 하여 바람에 돛을 단듯 매사가 순탄하다.
	     <li>천마가 청룡과 동궁하면 비룡마(飛龍馬)라 하고 命,身궁이나 천이궁에서 그러하면 극귀하게 되고 자녀궁에서 만나면 귀한 자식을 두게 된다.
	     <li>천마가 삼태와 동궁하면 태마(台馬)라 하여 귀하게 된다.
	     <li>여명이고 천마가 천량과 동궁하면 불륜이 반복된다.

	  




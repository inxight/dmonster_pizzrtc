             <li>천희가 재백궁에 거하면 변동이 심하여 재물을 모으기가 어렵다.
	     <li>천희가 천요와 함께 命,身궁에 거하면 성욕이 왕성하고 유년명궁에서 천희와 천요가 만나면 그 해에는 이성교제가 쉽게 이루어 진다.  

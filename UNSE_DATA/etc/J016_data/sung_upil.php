	     <li>우필이 천동과 동궁하는 여명은 유약한 성격으로 재취를 하거나 기혼자와 동거하게 되는 경우가 있다.
	     <li>좌보와 우필이 명궁, 재백궁, 관록궁, 천이궁에서 가회하면 대개 좋은 명으로 형모가 돈후하고 일생 많은 사람의 도움을 받으며 주성이 묘왕하면 평생 풍파가 없고 수명이 길다.

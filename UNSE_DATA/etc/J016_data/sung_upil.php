	     <li>부처궁이나 복덕궁에서 단수하고(좌보와 동궁하지 아니하고) 거문이나 파군과 동궁하며 살성이 보이면 혼인을 두번하게 되고 화기가 합세하면 삼각관계에 빠져 연애가 실패한다.
	     <li>우필이 명궁(命宮)이나 신궁(身宮)에서 주성없이 단수하면 박학다식하나 후처 소생이거나 다른 사람에 의해 길러진다.

             <li>지공은 육살성중의 하나로 음화(陰火)에 속하며 짝성으로 지겁(地劫)이 있으며 합쳐서 공겁이라 부른다.
	     <li>지공과 지겁이 명궁의 좌, 우에서 협명하면 노력을 많이하나 성취가 없어 허망하고 심히면 표탕한다. 그러나 명궁의 정성이 묘왕하면서 살성의 가회가 없으면 무방하다.
	     <li>명궁의 정성이 실함지에 있고 재백궁이나 복덕궁에 공겁이 있으면 일생 고단하고 빈천하다.
	     <li>명궁에서 지공이 단수하고(주성없이 홀로 있고)  묘왕지에 있으면서 재백궁, 관록궁, 천이궁에서 태양, 태음이 보이면 일찌기 대성하는 경우도 있다.
	     <li>명궁의 정성이 자미, 칠살, 파군화록, 무곡, 묘왕지의 탐랑이면 공겁이 동궁하여도 실천력이 있어 허망하지는 않다.
	     <li>명궁의 정성이 천기, 천량, 거문, 태음등이고 공겁이 동궁하면 매사 환상을 쫓으나 실행력이 없어 허무하게 된다. 
	     <li>자미, 탐랑이 지공과 동궁하면 탈속승의 사상이 있거나 출가를 하는 승도의 명이다.







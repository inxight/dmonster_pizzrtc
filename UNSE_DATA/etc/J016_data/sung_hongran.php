             <ul>
             <li>홍란은 음수(陰水)로 짝성으로 천희(天喜)가 있다.
	     <li>홍란은 천희, 대모(大耗), 함지(咸池), 목욕(沐浴), 탐랑, 염정, 천요등과 함께 도화성(桃花星)이다.
             <li>혼인을 하거나 하는 즐거운 일을 주관한다.
             <li>홍란이 재백궁에 단수하거나(혼자 있거나) 탐랑과 동궁하면 투기나 도박, 주색으로 재물이 모이지 않는다.
	     <li>유년命身궁이 삼방사정에서 홍란, 천희를 만나면서 문창이나 문곡을 만나면 그 해에는 결혼지사가 있다.
             <li>유년명궁이나 신궁에 칠살이나 경양이 있고 유년 질액궁에 홍란이나 천희가 보이면 그 해에는 수술을 하거나 피를 보게 된다.(血光之災)
	     <li>홍란이나 천희가 자녀궁에 거하면 딸자식이 많다.
             <li>홍란이 명궁에 거하면 대체로 조혼을 한다.
	     <li>홍란이 유년의 전택궁에 거하면 집안에 새 식구가 들어온다.
	     <li>여명이고 명궁에서 함지의 탐랑과 홍란이 동궁하면 기예나 가무가 뛰어나 연예계로 진출하면 길하다.
             <li>유년에서 홍란을 만날때 젊은 나이이면 혼인과 관련이 있고 중년이면 자손과 관련이 있으며 노년에는 상처(喪妻)의 운이 있다고 본다. 
	     <li>홍란이 천요와 함께 命,身궁에 거하면 성욕이 왕성하고 유년명궁에서 천희와 천요가 만나면 그 해에는 이성교제가 쉽게 이루어 진다.  

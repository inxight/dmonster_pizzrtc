             <li>천요는 소흉성으로 음수(陰水)에 속하며 풍류성이다.
	     <li>卯, 酉, 戌, 亥궁은 묘지이고 丑, 未궁은 함지이다.
	     <li>천요가 卯, 酉, 戌, 亥궁의 묘지에 있고 살성의 가회가 없으면 풍류가 있고 문예가 높아 부귀를 누리나  자칫 호음, 호색하여 풍류를 과하게 탐닉한다. 
	     <li>천요가 丑, 未궁은 함지에 거하면 음욕을 탐하고 홍란이 동궁하거나 대궁에서 충을 하면 주색으로 패가 망신을 자초한다.
	     <li>천요가 경양과 명궁에서 동궁하면서 길성이 보이지 않으면 형극이 심하고 요절의 명이다.
	     <li>천요가 身궁에 있고 삼방에서 탐랑이 보이면 음란하고 여명이면 투정이 심하다.
             <li>여명이 천요와 홍란이 동궁하면서 수신명(명궁이나 신궁에 있으면)하면 홍등가나 유흥업과 관련이 있다.
             <li>천요가 丑, 未궁의 함지에서 탐랑과 동궁하고 수신명하면 결혼을 두번한다.
	     <li>천요가 유년이나 대한 부처궁에서 홍란, 천희등과 만나면 미혼자는 연애결혼의 운이 있으며 기혼자는 중혼(重婚)의 계기가 있다. 
	     <li>천요가 재백궁에 있으면 주색이나 도박으로 파재하며 복덕궁에 있으면 염복이 많다.
	     <li>천요가 천이궁이나 노복궁에 있으면서 卯, 酉, 戌, 亥궁의 묘지이고 길성이 도우면 이성이나, 아랫사람, 귀인등의 조력을 얻는다.
	     <li>천요가 길성과 동궁하면서 명궁, 재백궁, 관록궁에 있으면 이성으로 인하여 재물을 얻는다.
	     <li>천요가 살성을 보거나 동궁하게 되면 이성을 멀리하여야 화를 면할 수 있다.






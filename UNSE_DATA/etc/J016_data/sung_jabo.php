             <li>좌보는 육길성중의 하나로  양토(陽土)에 속하며 짝성으로 우필(右弼)이 있고 합쳐서 보필이라 부른다.
	     <li>보필은 귀인성으로 친구, 동료, 동창등에서 나타나는 평배귀인(원조자)을 말한다.

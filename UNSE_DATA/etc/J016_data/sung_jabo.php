	     <li>부처궁이나 복덕궁에서 단수하고(우필과 동궁하지 아니하고) 거문이나 파군과 동궁하며 살성이 보이면 혼인을 두번하게 되고 화기가 합세하면 삼각관계에 빠져 연애가 실패한다.
	     <li>좌보가 염정과 동궁하면 관재송사나 재액이 있을 운이며 심하면 요절의 운이 있다.
	     <li>좌보가 명궁(命宮)이나 신궁(身宮)에서 주성없이 단수하면 박학다식하나 후처 소생이거나 다른 사람에 의해 길러진다.
	     <li>여명이고 명궁에 좌보가 있으면 전업주부일 가능성이 많다.
	     <li>염정화기가 경양과 동궁하면서 보필을 보면 재물을 탈취당하거나 도둑을 맞는다.
	     <li>좌보와 우필이 명궁, 재백궁, 관록궁, 천이궁에서 가회하면 대개 좋은 명으로 형모가 돈후하고 일생 많은 사람의 도움을 받으며 주성이 묘왕하면 평생 풍파가 없고 수명이 길다.
             <li>여명에서 염정과 보필이 동궁하면 형부극자(남편과 자식을 극함)한다.


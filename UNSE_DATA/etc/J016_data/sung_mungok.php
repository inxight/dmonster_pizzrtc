             <ul>
             <li>문곡은 육길성중의 하나로 음수(陰水)에 속하며 짝성으로 문창(文昌)이 있고 합쳐서 창곡이라 부른다.
	     <li>창곡은 귀인성으로 부하, 후배, 제자, 손아랫 사람등에서 나타나는 만배귀인(원조자)을 말한다.
             <li>문곡이 수명하면(명궁에 문곡이 있음) 이성으로 인한 문제가 끊임없이 일어난다.
	     <li>여명이고 문곡이 수명하거나  명궁의 삼방사정에서 창곡을 보고 도화성이 보이면 환상이 일어 바람을 피우게 된다.
	     <li>문곡, 무곡, 탐랑이 命궁에서 동궁하거나 命, 身궁에 나뉘어 있으면 午궁이나 丑궁이 행한으로 올때는 물로 인한 재난이 닥친다.
             <li>丑궁이나 未궁에서 태양,태음이 입명하고(명궁에 태양, 태음이 있는 경우) 좌우궁에서 문창, 문곡이 상협하면 부귀한 명이다. 
	     <li>명궁이 巳궁이고 未궁 복덕궁에 창곡이 있거나 명궁이 亥궁이고 丑궁 복덕궁에 창곡이 있으면 일생 부귀쌍전하고 염복이 많다.
             <li>寅궁이나 卯궁에서 파군과 동궁하면 인생에 파고가 많고 亥, 子, 丑궁에서 문곡화기가 파군과 동궁하면 일생이 고생스럽고 허망하다. 비록 길하다 해도 오래가지 못한다.
	     <li>염정이 巳, 亥궁에 있으면서 창곡이 命운으로 오는 행운에서는 목숨이 위험하다. 
	     <li>문곡에 화기가 붙으면 흉한 일이 발생하는데 부모궁에서 그러하면 문서로 인한 사기를 당할 운이거나 집안에 누군가가 죽어 상복을 입을 운이 있으며 재백궁에서 그러하면 수표등이 부도가 나 손재를 볼 운이다.
	     <li>또한 형제궁이나 노복궁에서 화기와 동궁하면 가까운 사람으로 부터 배신이나 사기를 당하며 부처궁에서 그러하면 이혼과 관련이 있다.
	     <li>문곡에 화과가 붙으면 각종 시험에 합격할 운이 있고 승진이나 취직등이 잘 되며 문서계약등이 잘 이루어지는 운이다.
	     <li>문곡이 묘왕지에 있고 삼방사정에서 길성이 모이면 재관이 뚜렸하고 부귀하며 과거급제의 운이 있으나 실함지에 있고 살성이 모이면 이름없는 달변가에 불과하다.
	     <li>천기가 함지에서 문곡과 동궁하면  생활이 안정되지 못하고 허세가 많으며 거짓을 좋아하게 된다.
	     <li>巳, 亥궁에서 탐랑과 동궁하면 하는 일이 거꾸로 되거나 갑작스러운 죽음을 당할 수 있다.
	     <li>문곡화과와 염정화기가 동궁하면 주색문제로 말썽을 일으킨다.
             




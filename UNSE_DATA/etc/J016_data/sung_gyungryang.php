             <ul>
             <li>경양은 육살성중의 하나로 양금(陽金)에 속하며 짝성으로 타라(陀羅)가 있으며 합쳐서 양타라 부른다.
	     <li>경양이 命, 身궁에 있으면 성격이 거칠고 폭력적이며 오만하며 호승심이 강하여 적을 쉽게 만들고 변동이 심하고 극단적이다.
             <li>특히 甲년생이나 庚년생이 卯, 酉궁에 수명하고 경양이 동궁하면 흉화가 극심하다.
	     <li>질액궁에서 천형이나 홍란과 동궁하면 혈광지재(피를 보는 일)가 일어난다.
	     <li>경양이 염정이나 화성과 동궁하면서 입명하면(명궁에 있으면) 소송사건이 있고 신체를 다치거나 한다. 
	     <li>경양이 전택궁이나 재백궁에 있으면 대체로 가난하게 산다.
	     <li>경양이 질액궁에서 주성없이 단독으로 있으면 중병이나 중상을 당한다.
	     <li>경양이 태양이나 태음과 동궁하면서 命, 身궁에 입하면 남자는 아내를 극하고 여자는 남편을 극하여 육친에 흉하고 재물이 모이지 않는다.
	     <li>경양이 화성이나 영성과 전택궁에서 동궁하면 화재를 당하는 수가 있으며 질액궁에 있으면 신체적으로 불구자가 되기 쉽고 몸이 온전치 않다.
	     <li>경양이 辰, 戌, 丑, 未궁에 있으면서 화성과 동궁하면 격발하는 계기가 있다. 이는 화살위권을 의미하며 살을 권으로 변화시킨다는 의미 이므로 군, 경, 사법, 의학, 과학계통에 종사하는 사람이면 잠재적인 격발의 운이 나타나는 경우가 많다. 
	     <li>경양이 亥, 子궁에서 탐랑과 동궁하면 범수도화격(泛水桃花格)으로 좋던 나쁘던  이성문제로 곤욕을 치르게 된다.
             <li>녹존이 있는 궁에 화기가 있으면 양타협기가 되어 주로 좋지 않은 재앙이나 실패, 손실, 좌절, 사고, 질병등의 악재가 생긴다.

